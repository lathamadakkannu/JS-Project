function foodfilter(a){
a.forEach(function(e){


var foodType=document.createElement("div")
foodType.classList.add("food-card")
foodType_Row.appendChild(foodType)


var foodType_img=document.createElement("div")
foodType_img.classList.add("food-img")
foodType.appendChild(foodType_img)

var food_image=document.createElement("img")
food_image.src=e.image
foodType_img.appendChild(food_image)

var foodType_header=document.createElement("div")
foodType_header.classList.add("food-header")
foodType.appendChild(foodType_header)
foodType_header.innerHTML=e.name

var foodType_offer=document.createElement("div")
foodType_offer.classList.add("food-offer")
foodType.appendChild(foodType_offer)
foodType_offer.innerHTML=e.offer

if(e.offer=="no Offer"){
        foodType_offer.remove()
}

var foodType_type=document.createElement("div")
foodType_type.classList.add("food-type")
foodType.appendChild(foodType_type)

var foodType_icon=document.createElement("i")
foodType_icon.classList.add("fa", "fa-star")
foodType_icon.setAttribute("aria-hidden","true")
foodType_type.appendChild(foodType_icon)
foodType_icon.innerHTML=" "+e.rating


var foodType_para=document.createElement("span")
foodType_para.classList.add("food-type")
foodType_type.appendChild(foodType_para)
foodType_para.innerHTML=" "+e.type

foodType_icon.style.color="#4F7942"

var foodType_price=document.createElement("div")
foodType_price.classList.add("food-price")
foodType.appendChild(foodType_price)
foodType_price.innerHTML="Rs. "+e.price

var foodType_place=document.createElement("div")
foodType_place.classList.add("food-place")
foodType.appendChild(foodType_place)
foodType_place.innerHTML=e.place

})
}




